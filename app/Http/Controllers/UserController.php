     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {

        $validate = $request->validate([
            'name' => 'required',
            'email' => 'required',
            'image_profile' => 'image|file|max:2048',
        ]);

        if($request->file('image_profile')){
            $validate['image_profile'] = $request->file('image_profile')->store('post-images');
        }
        
        // ddd($validate);
        
        User::where('id', $id)->update($validate);

        return redirect()->route('dashboard.profile')->with('success', 'Profile Updated');
    }

